package ch07;
public class Human {
	
	
 char type;
 String name;
 int age;

 // setter, getter 메소드 작성 마우스 우클릭 -> source-> setter and getter -> 세가지 체크 후 완료
 
public char getType() {
	return type;
}
public void setType(char type) {
	this.type = type;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public int getAge() {
	return age;
}
public void setAge(int age) {
	this.age = age;
}

 
 
 
 
 
 
}
