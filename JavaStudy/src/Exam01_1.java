
public class Exam01_1 {

	
		public static void main(String[] args) {
			System.out.println("1+2+3의 연산 결과는"+"'"+(1+2+3)+"'"+"입니다.");
			System.out.println("1+2+3의 연산 결과는"+"6"+"입니다");
			System.out.println("1~5 까지의 곱셉 결과는"+(1*2*3*4*5)+"입니다.");//곱하기는 우선순이라 괄호 안해도 가능
			
			//답
			System.out.println("1+2+3의 연산결과는 '"+(+1+2+3)+"'입니다.");
			System.out.println("1+2+3의 연산결과는 \""+(+1+2+3)+"\"입니다."); //"옆에 "를 더 쓸 경우 [\"]라고 표기한다.
			System.out.println("1~5 까지의 곱셉 결과는"+1*2*3*4*5+"입니다.");
			
		}
}
